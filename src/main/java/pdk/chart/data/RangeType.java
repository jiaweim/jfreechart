package pdk.chart.data;

/**
 * Used to indicate the type of range to display on an axis (full, positive or
 * negative).
 */
public enum RangeType {

    /**
     * Full range (positive and negative).
     */
    FULL,

    /**
     * Positive range.
     */
    POSITIVE,

    /**
     * Negative range.
     */
    NEGATIVE

}
