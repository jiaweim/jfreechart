package pdk.chart.axis;

/**
 * Used to indicate one of three positions within a category:
 * {@code START}, {@code MIDDLE} and {@code END}.
 */
public enum CategoryAnchor {

    /**
     * Start of period.
     */
    START,

    /**
     * Middle of period.
     */
    MIDDLE,

    /**
     * End of period.
     */
    END

}
